#include <iostream>
using namespace std;
int main(){
    //Character
    char sensor_type = 'n';
    cout << "Sensor Type: "<< sensor_type  << endl;
    //Integer
    int sensor_id = 12;
    cout << "Sensor ID: "<< sensor_id  << endl;
    //Float
    float temperature_celsius = 12.22;
    cout << "Temperature recorded: "<< temperature_celsius<<" Celsius" << endl;
    //Double
    double humidity_recorded = 40.3333;
    cout << "Humidity recorded: "<< humidity_recorded << endl;
    //Boolean
    bool active_status = true;
    cout << "Sensor is Active: "<< active_status<< endl;
    //Short Integers
    short level_of_recording = 1213;
    cout << "Recording Level: "<< level_of_recording  << endl;
    //Long integers
    long time_of_recording = 12435646456;
    cout<< "Time: "<< time_of_recording  << endl;
    //Unsigned Integer
    unsigned int workers = 2341234;
    cout<< "Number of workers: " << workers  << endl;


}